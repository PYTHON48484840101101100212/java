public class forfor {

    public static void main(String[] args) {

        int i;

        for(i = 1 ; i <=10 ; i+= 2){

            System.out.println(i);

        }

        System.out.println("      " +
                "                ");

        for(i = 2 ; i <=10 ; i+= 2) {

            System.out.println(i);
        }
System.out.println("    " +
        "     " +
        "");
        int k = 1;

        while(k<=10) {

            System.out.println(k);
            k+= 2;

        }

        System.out.println("      " +
                "      ");
        int e = 2;
        while(e<=10) {

            System.out.println(e);
            e+=2;

        }

        System.out.println("    " +
                "     " +
                "");
int j = 1;
        do {
            System.out.println(j);

            j+=2;
        }
        while (j<=10);

        System.out.println("     " +
                "    ");

        int t = 2;
        do {
            System.out.println(t);

            t+=2;
        }
        while (t<=10);



    }

}
